class Multi1a9 { // Clase Multi1a9
    public static void main(String[] args) { // Método main
        for (int i = 1; i <= 9; i++){ // Ciclo for
            System.out.println("Tabla de multiplicacion de " + i); // Impresión
            for (int j = 0; j <= 10; j++){ // Ciclo for
                System.out.println(i + "*" + j + " = " + i * j); // Impresión
            } // Cierre ciclo for
        } // Cierre ciclo for
    } // Cierre método main
} // Cierre clase Multi1a9