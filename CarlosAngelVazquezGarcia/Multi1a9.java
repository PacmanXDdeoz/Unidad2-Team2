class Multi1a9 {
    public static void main(String[] args) {
        for (int i = 1; i <= 9; i++){
            System.out.println("Tabla de multiplicacion de " + i);
            for (int j = 0; j <= 10; j++){
                System.out.println(i + "*" + j + " = " + i * j);
            }
        }
    }
}