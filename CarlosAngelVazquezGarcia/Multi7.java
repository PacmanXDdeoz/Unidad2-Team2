class Multi7 { // Clase Multi7
    public static void main(String[] args) { // Método main
        int a = 7; // Declaración y asignación
        for (int i = 0; i <= 10; i++){ // Ciclo for
            System.out.println(a * i); // Impresión
        } // Cierre ciclo for
    } // Cierre método main
} // Cierre clase Multi7