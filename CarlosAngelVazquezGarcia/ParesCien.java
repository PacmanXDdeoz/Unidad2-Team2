class ParesCien { // Clase ParesCien
    public static void main(String[] args) { // Método main
        int a = 100; // Declaración y asignación
        for (int i = 0; i <= a; i++){ // Ciclo for
            if (i % 2 == 0){ // Condicional if
                System.out.println(i); // Impresión
            } // Cierre condicional if
        } // Cierre ciclo for
    } // Cierre método main
} // Cierre clase ParesCien