class ParesMil { // Clase ParesMil
    public static void main(String[] args) { // Método main
        int a = 2; // Declaración y asignación
        while (a <= 1000){ // Ciclo while
            System.out.println(a); // Impresión
            a+=2; // Asignación
        } // Cierre ciclo while
    } // Cierre método main
} // Cierre clase ParesMil