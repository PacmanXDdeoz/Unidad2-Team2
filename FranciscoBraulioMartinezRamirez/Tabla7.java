class Tabla7{ //Inicio de clase
 public static void main(String[] args){ //Inico metodo
  System.out.println("Tabla del 7:\n"); //Titulo
  for ( int i = 1 ; i <= 10; i ++ ){ //Bucle 
   int num = 7 * i; //Accion de multiplicar 
  System.out.println(" 7 x " + i + " = " + num); //Muestra de la tabla
  }//Cierre bucle
 }//Cierre metodo
}//Cierre clase
